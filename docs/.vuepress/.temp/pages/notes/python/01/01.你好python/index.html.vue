<h2 id="_6-python-解释器" tabindex="-1"><a class="header-anchor" href="#_6-python-解释器" aria-hidden="true">#</a> <strong>6.Python 解释器</strong></h2>
<p>首先，一个基本原理是：</p>
<p><strong>计算机只认识二进制，即：</strong> <strong>0</strong> <strong>和</strong> <strong>1</strong></p>
<p><img src="@source/notes/python/01/01.你好python/assets/image-20230810105442440.png" alt="image-20230810105442440"></p>
<p>其实很简单，计算机是不会认识 Python 代码的。但是 Python 有解释器程序，如下图</p>
<p><img src="@source/notes/python/01/01.你好python/assets/image-20230810105514488.png" alt="image-20230810105514488"></p>
<p>安装 Python 环境，本质上，就是在电脑中，安装：</p>
<p><strong>Python 解释器程序</strong></p>
<p><strong>代码，随时可以写，但能不能运行，就要看电脑里面有没有解释器程序了。</strong></p>
<p>Python 解释器，是一个计算机程序，用来翻译 Python 代码，并提交给计算机执行。</p>
<p>所以，它的功能很简单，就 2 点：</p>
<ol>
<li>
<p>翻译代码</p>
</li>
<li>
<p>提交给计算机运行</p>
</li>
</ol>
<p><strong>问：</strong> <strong>解释器我明白了，可是解释器在哪呢？</strong></p>
<p><strong>答：</strong></p>
<p>解释器存放在：<code v-pre>Python 安装目录</code> /<strong>python.exe</strong></p>
<p><img src="@source/notes/python/01/01.你好python/assets/image-20230810105712485.png" alt="image-20230810105712485"></p>
