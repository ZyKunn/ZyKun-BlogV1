<p>不使用解释器，计算机不认识 Python 代码哦</p>
<p><img src="@source/notes/python/01/01.你好python/assets/image-20230810105834290.png" alt="image-20230810105834290"></p>
<p><strong>使用 Python 解释器程序，就能执行 Python 代码了</strong></p>
